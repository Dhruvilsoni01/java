
public class predefine_method {
	
	public static void main(String[] args) {
		// Every time it will generate the random number
		
		double num= Math.random();
		System.out.println("Random number is : " + num);

		// Type casting
		int num1=(int)(Math.random()*100);
		System.out.println("After type casting value is :"+num1);
	}

}
